package com.workday.custom.aunit.int0025.utilities;

@SuppressWarnings("unused")
public class StudioStarterKitAunitConfigurationException extends Exception {

	private static final long serialVersionUID = 5361779369004035631L;

	public StudioStarterKitAunitConfigurationException(String message) {
		super(message);
	}

	private StudioStarterKitAunitConfigurationException() {
		super();
	}

	private StudioStarterKitAunitConfigurationException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	private StudioStarterKitAunitConfigurationException(String message, Throwable cause) {
		super(message, cause);
	}

	private StudioStarterKitAunitConfigurationException(Throwable cause) {
		super(cause);
	}
}
